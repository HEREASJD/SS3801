	<h1 class="title_header">Geothermal</h1>
	<h2>What is it?</h2>
	<p>
		Geothermal power is generated by taking heat from deep within the Earth's crust and using it to turn water into steam. 
		That steam is then passed to a turbine, which does the actual energy generation.
		The heat can be transported through piping directly connected to the heat source (closed-loop system) or by pumping water to and from the heat source (open-loop system).
	
	</p>
