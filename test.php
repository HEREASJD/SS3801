	<h2>Should Michigan use it?</h2>
	<p>No. Geothermal is a bad fit for Michigan, but not because of the social and environmental concerns.
	While Michigan does have the sort of mining infrastructure to go looking for hot spots, the state itself simply 
	does not have the hot-spots necessary to allow for geothermal energy production to be a serious contender for Michigan's renewable energy.
	<br><br>If the state did have such hot spots, then their location matters greatly. If located near Michigan's agricultural sectors, then
	the risk of affecting nearby farmsteads is likely too great to make geothermal the driving force of renewable energy. Otherwise, that comes down
	to the question of if the people near such hotspots are willing to live with the consequences of constructing and running a geothermal power plant.</p>
