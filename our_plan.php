	<h2>Michigan's Energy Goals</h2>
	<p>
		Michigan's renewable energy goal is to achieve a renewable energy credit portfolio of 60% by 2035. 
		The team proposes a multi-faceted plan to achieve Michigan's renewable energy goals with focuses on wind and solar energy sources. In addition to further investment in renewable energy infrastructure, 
		Michigan will need to leverage tax incentives, explore energy storage solutions, and continue to invest in workforce development.
	
	</p>
	
