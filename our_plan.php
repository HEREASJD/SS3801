	<h2>Wind</h2>
	<p>
		Wind is the most feasible energy source for implementation on a large scale in Michigan. Large, open spaces in the form of farmland or offshore on the Great Lakes provide many locations for consistent, high speed winds. A combination of onshore and offshore wind would be capable of meeting Michigan's renewable energy needs. 
		Onshore wind farms should be expanded in the thumb region and in the Gratiot County area, where winds are ideal for wind power. In addition to onshore wind, development of offshore wind farms holds potential for generating a large amount of the state's energy and would help the state make large strides towards the state's goals.
		Lake Huron and Michigan are ideal locations for their proximity to power transmission infrastructure, relatively calm conditions, and access to reliable, high speed winds.
	</p>
	
	<h2>Wind Energy Challenges</h2>
	<p>
		Despite high potential, wind energy faces several large social challenges. In general, people dislike the appearance, sound, and land use of wind turbines, particularily those who live near wind turbines. Electricity providers need to involve local communities in the planning process 
		during the development of wind farms to develop farms suitable to the local community. 
		
	</p>
	<p>
		Offshore wind faces additional challenges, and even today no offshore wind farms are present in Michigan. The Great Lakes holds cultural and ecological importance, resulting in less acceptance for offshore wind turbines. 
		People's aesthetic concerns are similar for onshore and offshore wind turbines with additional concerns for tourism. In the development of offshore wind farms, electricity providers must perform a study of the environment
		to avoid critical habitats and minimize disruptions to aquatic life. Site planners must also consult local stakeholders to address aesthetic and tourism concerns. Finally, engineers need to consider icy conditions in the winter during the design of the wind turbines.
	</p>
	
	<h2>Solar</h2>
	<p>
		Commercial solar should be implemented to cover a large portion of Michigan's energy generation during sunny months. In the case of home solar, for implementation it is crucial to convince homeowners to install panels. Be it monetarially or through some creative social processes, it will be costly. Not many are willing to foot the bill on their panels, even with a 30% Federal tax credit.
		Despite the low lighting concerns in Michigan, a relatively small installation on every home would effectively cover the residential portion of the power grid and decrease the capacity required of other potential energy sources. 
		Mandating enough solar panels for self-sufficiency on all new constructions, while a tough sell, would reduce the strain on other forms of generation and potentially cascade into more previously-built homes installing panels through a form of peer-pressure.
	</p>
	
	<h2>Solar Energy Challenges</h2>
	<p>
		Similar to wind, solar energy faces backlash from some residents about aesthetics and fears of decreasing property values. The government must work with local communities to carefully implement solar projects.
	</p>
	<p>
		A concentrated solar power plant may help produce a significant portion of the power that Michigan needs to produce, but Michigan has subobptimal conditions for concentrated solar that would reduce the efficiency and cost effectiveness. Because of this, alongside the large upfront cost, wind power or other solutions are probably better solutions, and a concentrated solar plant is not recommended.
	</p>
	<p>
		Due to low reliability in the winter months, solar energy production will need to be offset by other energy production or storage. Sources such as natural gas and biomass could serve to supplement solar energy production during winter months.
	</p>
	
	<h2>Biomass</h2>
	<p>
		Biomass can provide additional energy production by turning waste into energy. Facilities can burn tree trimmings, select construction waste, and other wood-based products. Implementation on a large scale would require the growth of plants for biomass energy conversion. Switchgrass and hybrid popular should be grown to provide 
		fuel for biomas conversion.
	</p>
	
	<h2>Biomass Energy Challenges</h2>
	<p>
		Biomass is a renewable energy source, but the conversion of energy in biomass plants produces many pollutants harmful to the environment and society. Biomass plants should be constructued away from vulnerable communities and local stakeholders should be involved in the planning process. Biomass can serve as an important step towards renewable energy that can provide the reliability sources like wind and solar can't while 
		polluting less than coal alternatives.
	</p>
	
