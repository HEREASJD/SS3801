	<h2>Hydropower</h2>
	<p>
		Hydropower is a clean, renewable source which could be expanded to contribute around 5% of Michigan's energy production. Hydropower benefits local communities with cheaper energy, waterfront property, and new jobs.
	</p>
	
	<h2>Hydropower Energy Challenges</h2>
	<p>
		Hydropower requires a substantial upfront investment to build a hydroelectric power plant and requires the modification of the environment to create a dam. Careful planning is necessary to minimize the impact to the local environment. 
	</p>
	
	<h2>Tax Incentives</h2>
	<p>
		Michigan must expand tax credits for renewable energy projects for both businesses and residents. In particular, tax credits for solar and land easements for community wind 
		could help to substantially expand renewable energy. 
	</p>
	
	<h2>Energy Storage Development</h2>
	<p>
		Primarily relying on energy sources such as wind and solar provide additional concerns with reliability. While wind and solar can provide enough energy to power the state, varying weather conditions affect
		the ability to reliable produce enough energy. The state must support the research and development of battery storage to store energy during periods of excess generation and stabilize the grid during periods of weak solar and wind energy production. Additional technologies like pumped storage or hydrogen storage should also be explored to improve energy reliability.
	</p>
	
	<h2>Workforce Development</h2>
	<p>
		Expansion of renewables creates jobs in manufacturing, construction, and maintanance. However, workers must be trained to fill these new roles. Michigan must continue to 
		support training for employees looking for work in renewable energy. Initiatives such as the Clean Energy Workforce Development Program are necessary to develop the required workforce for renewable energy.
	</p>

	<h2>Conclusion</h2>
	<p>
		Michigan's ambitious goal of 60% renewable energy by 2035 will require investment across wind, solar, biomass, and hydropower. Beyond a monetary investment, the government and electricity providers will need to include local stakeholders to ensure new projects minimize social and environmental impact while maximizing societal benefit.
		Tax incentives are a necessary step to encourage development of renewable energy. To meet Michigan's energy demand with renewable power, the state will need to development energy storage to manage the lower reliability of solar and wind. Finally, the state needs to support training to development the workforce required to construct and maintain
		renewable energy facilities. Reaching Michigan's goal of 60% renewable energy is a very important step to more sustainable energy production which is healthy for the environment and society.
			
	</p>


