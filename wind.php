	<h2>What is Wind Energy?</h2>
	<p>
		Wind is a source of reliable, clean, and renewable energy. Wind turbines convert the kinetic energy in wind
		into electricity when the wind causes turbine blades to spin a generator. To maximize energy generation, wind turbines are best placed in locations
		with consistent windy conditions. Additionally, wind turbines are placed on tall towers and rotate to face the wind to improve energy generation. One wind turbine
		can generate electricity ranging from 5 kW to over 5 MW depending on wind turbine size and conditions.
	
	
	<h2>How Much Energy can Wind Generate in Michigan?</h2>
	<p>
		Michigan is the 14th most wind-abundant state. Michigan has the potential to generate approximately 16,500 MW using onshore wind turbines and could achieve over 300,000 MW 
		with offshore production. In 2023, Michigan had an installed wind energy generation capacity of 3,768 MW. Michigan had over 1,500 wind turbines which 
		produced 8.4 TWh in 2023 or ~9% of the 89 TWh consumed in 2023.
	</p>
	
	<h2>How Much Does Wind Energy Cost?</h2>
	<p>
		Electricity providers pay the initial cost of building wind turbines and then costs are passed onto consumers in the form of increased rates. It costs around $2.5 to $4 million dollars 
		to build a commercial wind turbine with a ~3 MW capacity. Offshore wind turbines have higher capacities at higher costs of $20 to over $100 million dollars for a turbine which can produce ~9 MW. 
		The price is passed down to consumers, however, regulations limit the allowable increase in energy rates to $3.00 for residential/month and $16.58 for commerical/month.
	</p>
	
	<h2>Does Michigan have the Required Workforce?</h2>
	<p>
		Wind energy, among other renewable energy sources, requires a skilled workforce to manufacture, install, and maintain wind turbines. Investment in renewable energy creates 
		jobs which can boost the economy, however, workers must have the skills and training to succeed in these new jobs. In 2024, Governor Gretchen Whitmer announced a $129 million investment,
		part of which will support workforce development programs focused on ensuring Michigan has the workforce required to meet the state's energy goals. With this investment, Michigan
		should have the workforce it requires to sustain the develop of renewable electricity production.
	</p>
	
	<h2>What are the negative impacts of wind turbines?</h2>
	<p>
		Wind turbines are not aesthetically pleasing to many people, which can cause some push back on the development of more wind energy. Fortunately, wind turbines are best suited in rural areas
		where few people are required to frequently view wind turbines. Additionally, wind turbines can cause shadow flicker as light passes through the blades. Wind turbines also emitt a low audible noise of around 50 dB. 
		Both shadow flicker and turbine noise are limited to a very short range, limiting any impact to a temporary nuisance for passerby. 
	</p>
	
	
	
