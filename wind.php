	<h2>Should Michigan use it?</h2>
	<p>	Yes! Wind energy is a clean, renewable, and reliable source of energy. Michigan has sufficient space for wind energy to contribute a significant portion of Michigan's energy production.
		Wind energy can provide jobs in manufacturing, construction, and maintenance which can boost the economy. Despite a high upfront cost, over time wind becomes more cost effective than fossil fuel alternatives
		like coal power. Land easements or leases can even aid local communities by providing a source of income. Finally, investment in wind energy is certainly worth a healthier climate, environment, and world.
	</p>
	
