	<h2>Is Home Solar viable?</h2>
	<p>Unfortunately home solar is very difficult to implement retroactively. To convince homeowners to install solar would be very expensive. 
	Many factors contribute to how likely someone is to do so, and there is a very hefty price to pay to overstep these boundaries. 
	The government would need to pay for much more than a third of the cost -- potentially even pay up outside of the realm of taxes -- when it comes to installing panels to put them on already built homes to any substantial degree. 
	<br></br>
	That being said, the government could likely introduce new guidlines for buildings, 
	potentially requiring every new home built to install at the very least enough panels to power the average home through most of the day. 
	This would lessen the impact of new homes on the planet and slowly but surely put the idea into residents' heads to install their own.[14]
	<br></br>
	Home solar, if someone is willing to spend the money to put an array on every house, is a stellar option for achieving carbon neutral goals.</p>
