	<h1 class="title_header">Michigan's Current Plan for Renewable Energy.</h1>
	<link rel="stylesheet" href="styles.css">
	<p>
		Michigan has implemented new public acts in order to crack down on Renewable Energy practices.<br>
		<br>
		Public Act 229 involves overseeing utility-scale solar, wind, and energy storage projects under Public Act 233.<br>
		The Michigan Public Service Commission (MPSC) has directed its staff to hold open meetings with experts, local governments, developers, and other stakeholders starting in March 2024.<br>
		The staff will provide recommendations on application filing instructions, renewable energy ordinances, and related issues by June 21, 2024 [8].
	</p>
	<p>
		Public Act 231 enables the Michigan Public Service Commission to address climate change, environmental justice, and affordability in long-term energy planning.<br>
		It also begins studies on energy waste reduction, demand response, and electrification of transportation buildings, and industry by September 30, 2024 [8].<br>
		They also must submit updates to the Michigan Integrated Resource Planning Parameters and Filing Requirements and a straw proposal for clean energy plans by September 30, 2024 [8].
	</p>
	<p>
		Public Act 233 grants the Michigan Public Service Commission the authority to site large-scale solar, wind, and energy storage production under certain conditions.<br>
		The Michigan Public Service Commission has directed its staff also to hold meetings with their stakeholders starting in March of 2024<br>
		and submit recommendations on applicant filing instructions, renewable energy ordinances, and other issues by June 21, 2024 [8].
	</p>
	<br>
	<p>
		Michigan's Renewable Portfolio Standard (RPS) in 2021 was increased from 10% to 15%. This standard applied to all retail electric providers.<br>
		These included companies within investor-owned utilities, cooperative utilities, municipal utilities, and alternative electric suppliers.<br>
		To achieve the 15% standard, investor-owned utilities must continue retiring Renewable Energy Credits (RECs), while others can voluntarily do so.<br>
		The RECs represent one megawatt-hour of renewable energy and are generated from wind, solar, biomass, waste-to-energy, and hydroelectric sources.<br>
		Incentive credits are provided for specific types of renewable energy, such as on-peak generation or stored off-peak generation used during peak hours.<br>
		Energy waste reduction credits can partially substitute RECs.<br>
		Most renewable energy under the RPS is Michigan-generated, with wind as the leading resource and solar rapidly growing.<br>
		The MPSC issues an annual report on RPS progress. [11]
	</p>
	<p>
		As of today, the MPSC has implemented the public acts above (a couple not mentioned) that grant them authority for utility-scale clean energy projects<br>
		that aim to raise the RPS for Michigan utilities to 60% by 2035 and 100% by 2040. They also want to raise energy waste reduction targets [8].
	</p>
