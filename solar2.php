	<h2>Should Michigan use it?</h2>
	<p>While concentrated solar is quickly growing more and more efficient, it is not a great pick to provide a majority of Michigan's power, due to its lower energy production in the winter months, when Michiganders will need the most power. Michigan also receives far less sunlight than most of the places where concentrated solar plants have been built, meaning that to produce similar amounts of power as those plants, more area and more mirrors would be needed.
	<br><br>If a concentrated solar plant was built in Michigan, it could potentially be extremely helpful in offsetting carbon emmissions by reducing the amount of power that is needed to be produced using other sources. However, concentrated solar is not currently a good option to supply a majority of Michigan's power, due to the seasonal variations in production, the large initial cost, and the non-ideal amount of sunlight that Michigan receives.</p>
