	<h2>What is it?</h2>
	<p>
		Concentrated Solar Power concentrates sunlight with mirrors in order to collect it more efficiently. The mirrors are built in a variety of ways, including large parabolic curves and arrays of smaller dual-axis heliostats. There are also a variety of ways to produce electricity from the concentrated rays, including using heat to power a steam engine or stirling engine, and shining it directly onto specialized photovoltaic cells. Some concentrated solar systems are also used to heat up components of industrial processes directly.
	
