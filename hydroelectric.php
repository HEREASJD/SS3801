	<h2>Should Michigan use it?</h2>
	<p>Michigan should not move towards hydroelectric power. Due to recent regulations on dams, hydroelectric power has seen a major decrease in energy output down to 40% of its previous output. 
		This compounded by the costs of investement into new dams and the growing costs of their maintenance points towards it not being the optimal choice for Michigan's future expense and production wise. 
		 <br></br>
		 Hydroelectric power also fails on the local level of society. Although it does provide economic output for the local communities 
